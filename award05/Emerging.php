    <td colspan="2" class="b">
	<p>In order to be considered for this award, the nominee must have demonstrated leadership and achieved breakthrough results in health informatics, been active in promoting and developing the field through participation in industry associations, volunteer boards, publications, conference presentations, or academic organizations and also be recognized by managers and peers for demonstrating innovation, leadership and a significant contribution to the field.  </p>
	<p>Progress in the Health Informatics community is dependent on nurturing strong and passionate leaders that will continue to move the health informatics agenda forward and shape our future. Despite being relatively new to the industry, Nancy Gabor has demonstrated the commitment, energy and passion that are hallmarks of an emerging leader.  She began her professional life as a national and international healthcare market researcher - that took her as far afield as London, Paris and Bangkok.  Through this work she recognized a need for industry focus on the appropriate use of technology in healthcare and, in 2002, decided to commit to e-Health as a personal and career focus.  She started taking courses and building her network with her goal - To be part of a process that aims to empower consumers to own their own health!</p>
	<p>Our Emerging Leader's first opportunity to demonstrate her leadership skills occurred in 2003 when she was hired as an e-Health Analyst by the Ontario Hospital Association.   In this capacity, Nancy worked as a Project Manager and a Working Group Manager and was really a catalyst to bring important e-Health issues to the forefront of the political agenda.  Some notable examples of her work include managing the Ontario Hospitals Regional Registries Group and assisting with the development and implementation of the OHA's 2005 EHR Readiness Survey, which provided hospitals across Ontario with valuable insights as to their individual and collective 'readiness' to engage in the province's e-Health Strategy.</p>
	<p>In these roles, Nancy's interpersonal skills enabled the facilitation and nurturing of relationships among, and between, multiple levels of stakeholders, an important component of work in the informatics field.  In addition, demonstrated project management skills and a comprehensive understanding of the policy and organizational requirements of e-Health, provided critical direction that enabled success. Our emerging leader has recently made a career move and is now applying her research, evaluation and leadership skills to the telehealth environment. </p>
	<p>We all know that being a leader is often exemplified by the contribution one makes to the industry outside of the workplace.  Nancy has clearly demonstrated commitment and passion by becoming involved.  A member of COACH since 2003, she volunteered for and became an active member of the Professional Development Working Group and, more recently has assumed the Chair of COACH's Code of Ethics Task Force.  She is also an active member of the American Medical Informatics Association, HIMSS, the Canadian Society of Telehealth and the Canadian Evaluation Society.</p>
	<p>This year, Nancy has also become a student in the first cohort of a prestigious distributed-stream graduate program in Health Informatics at the University of Victoria.  There is no doubt that her studies will provide an academic background that, when combined with her practical experience and natural skills, will enable her to assume critically needed leadership and ensure the continued advancement of e-Health in Canada.  </p>

