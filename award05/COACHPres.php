<tr>
    <td colspan="2" class="b">
	<p>This year's winner, Elaine Sawatsky, has contributed immensely, and in many ways to our Association. As a well known and respected consultant in the area of privacy and security, she was a major leader in, and contributor to, the design and content of COACH's Privacy and Security Guidelines.  Under her leadership, the Guidelines have undergone a major revision and have now been accepted as a licensed, provincial-level tool, for use across a province's entire public and private healthcare stakeholder group. She continues to champion the Guidelines use and impact to her direct community of privacy and security contacts and also within the larger Health Informatics Community. With the new guidelines licensing program, our entire Association will benefit for many years into the future and we applaud her work as a major contributing member.</p>
	<p>In addition to this effort, her contribution to the larger health informatics standards community in her work, "A Code of Practice for Information Security Management", is also recognized.  </p>
	<p>As a very successful President of BCHIMPS, she ensured that its link with COACH remained strong.  Not only has she supported her local association for many years, she has contributed to COACH beyond her work as a committee chair - in strategic planning sessions, board meetings, COACH working groups and conference program committees, and is an untiring supporter and promoter of the Association in the field.  </p>
	<p>She continues to show positive support of the entire health informatics community by sharing her knowledge with new recruits, students and junior members through her work with the University of Victoria, the CIHI Partnership for Standards and members of other provincial ministries.  To strengthen her knowledge base, she commits to ongoing professional and personal development.  Respected completely by her community of peers, clients and associates, COACH acknowledges Elaine Sawatsky's outstanding contributions to the Health Informatics Community and presents her with the President's Award for 2005!</p>

