    <td colspan="2" class="b">
	<p>In Ontario, community health care is provided by private companies who are awarded contracts through an RFP process.  As such, there is no financial support for technological development or governmental provision of e-health systems to bring these providers into the progressive technology stream along which the entire health care system is moving.  Yet these players are key to the total e-health strategy. In 2004 in Ontario alone, 30,000 healthcare workers delivered in-home health care services to 563,000 citizens.  These workers must have accurate and timely information about their patients and must communicate with a vast number of service providers and agencies.  State-of-the-art community health care service depends on the connectivity of its workers with primary health care service coordination and provision.  This is true for any locale but especially critical in rural environments.  Timely information is also critical to promoting the safety of community health care workers who, by nature of their work, function in isolation and thus are exposed to personal risk.</p>
	<p>Medshare Corporation has developed a portal that provides an easy-to-use intranet point that can be accessed through the internet using a standard internet explorer browser.  They provide login authentication, access control, and encryption to ensure the protection of patient privacy and meet strict government privacy legislation requirements.  Out-bound workers can connect securely from home offices and other remote locations.  Reduced administrative and clerical costs, and improved employee satisfaction with enhanced overall efficiency have resulted, due to improved communication, information transfer and connection with head offices.  </p>
	<p>With this technology, Agencies now have a platform on which to make the transition to paperless electronic health records.  Reliable security, a cataloguing system that permits rapid storage and retrieval and a failsafe backup system, set the stage for on-site medical records storage.  </p>
	<p>Seen by their client as being poised to revolutionize community heath care information management, nationally and internationally, and deserving to be recognized for their progressive leadership in the health informatics field, Medshare Corporation is recognized for the implementation of the MedShare Portal with Therapy Partners.</p>

