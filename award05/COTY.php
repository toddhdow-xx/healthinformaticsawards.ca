	<p>In order to determine the winning company of this most prestigious honour, it was necessary to establish measurable criteria on which to evaluate submissions in the areas of relationship management, delivery, product and service offering, depth of skill, growth and commitment to research and development in the health informatics industry.  Our winner was nominated three times by its business partners and customers, and exceeds expectations, is committed to achieving successful results, meets tough challenges head-on and successfully delivers what it proposes with demonstrated innovation. </p>
	<p>This year's inaugural winner of Company of the Year, Clinicare, provides a complete software solution to medical groups within the ambulatory healthcare market that assists in improving the delivery of quality healthcare to patients, enhances medical records access and increases administrative and operational efficiency.</p>
	<p>Clinicare continuously develops their product with client guidance and direction.  Their customers provide feedback and outline their requirements and the company responds with how they can meet or exceed those expectations and commits to delivery with dates that they are measured against openly with their clients.  They believe that customer support starts with a face-to-face engagement and establishing an on-going relationship.</p>
	<p>The founder, Dennis Niebergal brings immense experience with his clinician background and has established an advisory board of practicing clinicians.  In this way, the company can quickly react to the changes that are occurring in the healthcare sector and apply it to the development of applications.   They have over 20 developers with various scientific backgrounds to ensure their applications will quickly adapt to the new world of personalized medicine.  They commit to keeping abreast of the ever-changing landscape of the healthcare system, in Canada, the United States and globally. No two site requirements at almost 200 installations in their medical clinics throughout North America have been the same, proving that Clinicare can provide innovative, creative thinking to realize objectives.</p>
 	<p>Their commitment to research and development in the health informatics industry is unsurpassed and their involvement in provincial, national and international healthcare IT initiatives continues to be significant.  </p>
	<p>KLAS Enterprises, LLC, is a research and consulting firm specializing in monitoring and reporting the performance of healthcare IT software vendors.  Using data from 4000+ healthcare facilities and 300 HIT vendors, the firm analyzes over 500 different products in North America.  Clincare scored the highest marks of all vendors in 31 of the 40 criteria involved in the survey, and received for the second year in a row "First in KLAS" vendor for Ambulatory Physicians (6 - 25 Physician Groups), and were also recognized as the #1 EMR Vendor.  </p>
	
