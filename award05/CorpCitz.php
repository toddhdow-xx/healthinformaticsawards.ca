	<p>CHITTA has established the <strong>Corporate Citizenship Award</strong>, presented by Healthcare Information Management & Communications Canada, to recognize leading for-profit healthcare ICT companies that are committed to, and passionate about, health informatics - and who have demonstrated a significant investment in contributing to the industry in Canada. To qualify for consideration of this prestigious award, the private sector company must actively participate in promoting and developing Canada's Healthcare ICT industry, and be recognized by clients and peers for having a long-term commitment to the industry.   The company supports and presents at industry conferences and events, supports educational initiatives and academic organizations, participates in standards and other infrastructure activities and maintains an involvement with industry associations, volunteer boards, and publications.</p>
	<p>Recognition for this award falls into 3 categories: <strong>1. Multi-National Company; 2. Major Canadian Company with sales in excess of 10 million; and, 3. Small to Medium Enterprises with sales under 10 million.</strong>  This year, the Award was presented in 2 categories: Multi-National Company and Small to Medium Enterprise.</p>
