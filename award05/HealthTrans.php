    <td colspan="2" class="b">
	<p>Important criteria such as the ability to replicate the transformation in other Canadian jurisdictions, as well as the measurability of the positive impact on patient outcomes and/or the delivery of care were considered in the determination of awarding of this honour. </p>
	<p>The Fraser Health Authority, is one of, if not the largest health authority in Canada. They began a project in 2001 with a pilot implementation in Maple Ridge, a community served by the FHA, involving about two dozen homecare workers using a new Mobile Wound Care Solution from Webmed Technology Inc.</p>
	<p>The Pixalere Wound Care System enables homecare nurses to transmit patient information and compressed digital images over a standard wireless cellular network to a secure Web server in a central facility, such as a hospital. The Web server hosts the wound care management software as well as the database containing patient demographic information, treatment history, assessments and wound images.</p>
	<p>Consulting physicians or wound care specialists can quickly access and evaluate patient reports and wound photos from anywhere at any time via the Internet, using a standard browser. By accessing the system, they can pull up client files, view digital images, read the in-home assessments, and add their own comments and treatment recommendations.</p>
	<p>In urgent situations, the homecare nurse can specify that a specialist be alerted by either e-mail, SMS messaging or via a page, all of which are routed via the server to the consulting wound care specialist who can go online to review the case and send back instructions immediately. Once the consultation and recommendations have been added to the patient file, the homecare nurse is supported by being notified online. </p>
 	<p>Since the implementation of the program, results have shown that remote diagnosis and early, in-home treatment-start for minor wounds can significantly speed up the healing process, optimize the use of treatment specialists and homecare providers, and reduce healthcare costs by reducing the need for hospital visits.</p>
	<p>The ability to positively manage this condition that continues to have a high prevalence across the continuum of health care is proving to be cost effective in all aspects of resource management, from human resources to inventory control. </p>
	<p>This project is being used as an initial e-health application whose value will continue to be meaningful and relevant in how professionals will improve their practice with a system that provides a micro to macro scrutiny of numerous and varied data elements.   </p>
	<p>The application of this pilot implementation is only the first with Ostomy, Post-op, IV therapy and Palliative care modules soon to be developed which will only complement and complete a valuable resource tool for health care professionals in a variety of settings.</p>

