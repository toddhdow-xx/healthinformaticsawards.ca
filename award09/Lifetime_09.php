    <td colspan="2" class="b">
	
	<p>Richard Alvarez, President and CEO, Canada Health Infoway, presented the industry award and led the tribute to recognize Steven's accomplishments.  Richard shared a collection of recorded accolades and memories from Steven's friends and colleagues throughout the tribute.   </p>
	<p>Steven is President of the Canadian Healthcare Information Technology Trade Association (CHITTA), Editor/Publisher of Healthcare Information Management & Communications Canada (HIMCC), the official journal of CHITTA and COACH: Canada's Health Informatics Association, and Executive Director of the International Medical Informatics Association (IMIA).  A Registered Industrial & Cost Accountant (now CMA), Steven's interest in health informatics flourished while he filled senior administrative CFO and CIO roles with hospitals and government.  </p>
	<p>His many accomplishments include:<br>
	
	<li>Serving COACH in many capacities, including as Founding President, 1975-78; Executive Director, 1980-99, and Editor of the COACH history book, covering 1975-2005;</li>
	<li>
Being the only Canadian to serve as President of IBM's user group, Electronic Computing Health Oriented (ECHO), 1983-87;
</li>
	<li>
In 1987, starting HIMCC, the only health informatics magazine in Canada for many years;
</li>
	<li>
Together with David Wattling, founding  CHITTA, now the healthcare division of the Information Technology Association of Canada (ITAC), in 2002;
</li>
	<li>
Establishing the COACH Founding President's Award in 1983 to recognize and motivate outstanding health informatics students at the University of Victoria. In 1999, COACH established the Steven Huesing Scholarship for students in health informatics or related programs at Canadian post-secondary institutions. The scholarship reflects Steven's spirit, dedication and innovation in the field. 
</li>

