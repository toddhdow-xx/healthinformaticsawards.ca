New in 2011, this award honours a clinician who has shown outstanding leadership in advancing the use of health information communication technology (ICT) and informatics in clinical practice resulting in improved provider and patient experiences and outcomes. *this award has been merged with <a href="http://www.coachorg.com/en/careerdevelopment/communitybasedphysicianleaderinnovator.asp">Community-Based Physician Leader/Innovator Award</a>.<BR>
