<B>Criteria</B><BR>
Nominations must specifically address all the following criteria. Nominations must be complete; incomplete nominations will not be considered. 
<ul>
<li>Practicing clinician leaders at local, regional and/or national levels
<li>The nominee's clinical colleagues have recognized him/her as a champion for advancing the adoption and benefits such as adoption and benefits of EHRs, EMRs, EPR' and other such records or telehealth.
<li>The nominee has served as a role model, champion and leader in encouraging design, implementation and use of ICT to improve knowledge based practice and the quality of patient care.
<li>The nominee provides demonstrated leadership in encouraging the appropriate use of ICT in practice and the management of patient/client/resident care/education and improved health outcomes.
</ul>
<B>Examples</B>
<ul>
<li>Implementing and using an ICT in an innovative fashion
<li>Encouraging interconnectivity of ICT systems between health care providers and across the continuum of care
<li>Encouraging use of ICT to improve health outcomes, education, access, efficiencies, quality and patient safety
</ul>
<B>Eligibility</B><BR>
This award is open to any practicing clinician in Canada. CHIA reserves the right to award more than one individual in any given year.<BR>
