This award recognizes leading for-profit healthcare ICT companies that are both committed to and passionate about health informatics (HI) and have heavily invested in contributing to the industry in Canada. This first annual award provides client organizations and business partners the opportunity to nominate private sector companies who they feel have demonstrated a very high level of commitment to HI in the past 12 months.<BR>
