The award will be given to healthcare ICT companies as follows: 
<ol>
<li>Multi-national Company 
<li>Major Canadian Company (Sales in Excess of 10 M) 
<li>Small to Medium Enterprises (Sales under 10M) 
</ol>
<B><i>Presented by <a href="http://www.healthcareimc.com/bcovers/index.lasso">Healthcare Information Management & Communications Canada</a></i></B><BR>
<BR>
<B>KEY CHARACTERISTICS of these private sector companies include the following:</B>
<ul>
<li>Actively participates in promoting and developing Canada's healthcare ICT industry 
<li>Participates and presents at industry conferences and events 
<li>Supports educational initiatives and academic organizations 
<li>Participates in standards and other infrastructure activities 
<li>Involved in industry associations, volunteer boards, and publication
<li>Recognized by clients and peers for having a long term commitment to the industry
<li>Provides financial support to industry events such as sponsorships, patronage, etc. 
</ul>

<B>Criteria</B>
The nominating organization is to assess the healthcare ICT company based on the following four criteria: visible leadership, participation in associations and institutions, sharing experience and contribution to knowledge development and measurable level of support. Please provide a detailed description highlighting at least one example of how the company excels at each of the criteria. The description is limited to 300 WORDS MAXIMUM for each of the criteria.<BR>
<BR>
<B>Criteria 1: Visible Leadership</B><BR>
Provides visible leadership through the implementation of Canadian-adopted standards and in participating on panel discussions, speaking at conferences and events, participating in conference program committees, chairing health information standards committees, sponsoring events and exhibiting at conferences, etc<BR>
<BR>
<B>Criteria 2: Participation in Associations & Institutions</B><BR>
Participates in associations, volunteer boards, academic institutions, health information standards committees, and healthcare institutions, such as hospital, agencies or Community Boards. <BR>
<BR>
<B>Criteria 3: Shares Experience & Knowledge Development</B><BR>
Routinely shares experience and contributes to the national knowledge development by publishing and/or advertising in journal and magazines, contributing to the development of health information standards, etc. <BR>
<BR>
<B>Criteria 4: Level of Support</B><BR>
Measurable level of support identified by amount of resources contributed and diversity of support provided - Quantify human & capital resource investments in industry activities and forums. <BR>
<BR>
<B>Eligibility</B><BR>
Nominations must be forthcoming from clients, business partners, associations, educational institutions and standards organizations. Nominations will be accepted for companies with any number of years experience. Eligible companies will have a Canadian presence and a practice partially or entirely dedicated to health-IT.<BR>
<BR>
<B>Deadline: 5 pm EDT Sept.17</B><BR>
<BR>
<B>Recognition:</B><BR>
The award will be presented at the <a href="http://www.healthinformaticsawards.ca/">Canadian Health Informatics Awards Gala</a> in Toronto Nov. 7, 2012.<BR>

