    <td colspan="2" class="b">
	<p>Roger Girard certainly meets all of the eligibility criteria - and then some.  He is a familiar and well-known figure in the health informatics community.  His career in the healthcare information management has expanded over thirty years and has included CIO and other senior leadership positions in Manitoba, Quebec, Alberta and Ontario.  In addition to his contribution to COACH, our award recipient has shared his experience and expertise with the informatics community in a number of capacities.  As Assistant Adjunct Professor at the University of Calgary, as a frequent presenter at national and international conferences and as a past member of the Canadian Advisory Committee for ISO TC 215, which is responsible for the development of international standards within healthcare systems, he has contributed to the growth and knowledge sharing that is critical to our relatively young profession.</p>
	<p>In terms of contribution to COACH, Roger has been a member of COACH for decades. He is a founding member of COACH's Thirty Year Club - never one to miss an opportunity to share a dinner with old colleagues and reminisce.  He served on the COACH Board of Directors from 1983 to 1985.  More recently he has served as Chair of the COACH's Resolutions Committee and was a major contributor to the COACH's 30th Anniversary History of COACH book that was launched at last year's 2005 Canadian Health Informatics Awards. </p>
	
