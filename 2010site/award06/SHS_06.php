    <td colspan="2" class="b">
	<p>Throughout her career, she has consistently striven to demonstrate an attitude of excellence in approaching new challenges.  Her frustration with the limited application of advanced informatics in health care led her to enroll in the Masters program in the hopes of gaining the expertise and influence necessary to effect change.  </p>
	<p>To address the well-documented knowledge gap around diagnostic medical radiation, she and her partner researched the integration of an InfoButton into clinical workflow.  They coined their solution "Rad-Link", designing it to provide emergency physicians with context-sensitive knowledge around DMR integrated with patient specific cumulative radiation exposure and alternative diagnostic strategies derived from clinical practice guidelines.  This was presented to the academic community at a Health Informatics Seminar Series as well as at the 11th International Symposium for Health Information Management Research where it was nominated for Best Paper.</p>
	<p>Katrina was also selected to participate in the Institute for Applied Health Research and Design, based on her research proposal 'Use of Diagnostic Decision Support by Emergency Physicians and Trainees.  Her thesis,' Design of Adaptive, Patient-Specific Clinical Pathways from Practice Guidelines' is supported by funds from the Dalhousie Medical Research Foundation and AGFA Corporation.  Her ultimate ambition is to not only conduct research and contribute new knowledge in the emerging field of Heath Informatics, but to see the findings translated into practice-changing behaviours and policies, and, to convert her practical experiences in volunteer advocacy with Physicians for Global Survival (Canada) to the domain of health policy.</p>

