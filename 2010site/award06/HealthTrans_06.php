<tr>
    <td colspan="2" class="b">
	<p>Orion developed this clinical portal solution in cooperation with Capital Health clinician groups.  The Portal leverages Capital Health's underlying information systems, achieving regional, patient-centric health information without requiring the replacement of existing patient care systems.   </p>
	<p>The Portal displays patient health information in a consistent clinician-friendly manner across Capital Health's facilities and programs.  It provides the ability to review and manage clinical documents and results, to securely communicate with clinical users within the portal environment, and the ability to provide access to clinical information in both community and hospital settings.  Users of the Portal are able to collate clinical information from multiple sources onto one web screen all within a secure environment.  All user access to the portal is audited, and system administrators are able to restrict access to clinical information.</p>
	<p>The portal also provides measurable improvements in patient outcomes.  The availability of information has reduced the number of interventions by providing clinicians with up-to-date and comprehensive patient records, and has significantly reduced clerical time.  Today netCARE has more than 9800 users, and the number has been growing at eight percent per month for the past year.  The portal has enjoyed a high adoption rate by clinicians and has been embraced as a valuable tool in daily clinical activities.</p>
	
