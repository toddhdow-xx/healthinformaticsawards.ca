	<p>Important criteria such as the ability to replicate the transformation in other Canadian jurisdictions, as well as the measurability of the positive impact on patient outcomes, and/or the delivery of care are considered in the determination of awarding this honour. </p>
	<p>Canada Health Infoway's vision is to develop an interoperable, pan-Canadian Electronic Health Record.  Accessible by all qualified healthcare providers across Canada, this core data set will save the healthcare system billions of dollars and greatly improve the quality of patient outcomes.  The single-most important building block of this core data set is the patient medical record in the ambulatory setting.  This year's award highlights the transformation by Clinicare of a 55 physician group from an antiquated paper charting system to a State-of-the-Art Electronic Medical Record enabling future transfer of data to Infoway's EHR.</p>
	<p>The Winnipeg Clinic is one of the largest multi-specialty clinics in Canada and offers a wide range of diagnostic facilities and health related services to its patients. With some 600,000 paper charts, the Clinic embraced the transition from paper to digital records to improve efficiency and patient care.   The Clinic physicians are now writing prescriptions in a module that tracks Drug/drug contraindications, drug/allergy interactions and dosing.  Prescriptions are printed out for the patient or faxed directly to the pharmacy and recorded electronically in the patient's record.  </p>
	<p>As archived charts are gradually entered into the EMR, they are being stored off site creating newly available space for additional examining rooms.  After 6 months on the new EMR, physicians are sufficiently trained and are now realizing efficiencies with the system, in both time and overhead costs.  The technology is now scheduled to be implemented at a number of health centres throughout Canada in the near future. Through this project, Clinicare with the Winnipeg Clinic have proven to have fundamentally transformed healthcare in Canada through the use of health informatics.</p>
	
