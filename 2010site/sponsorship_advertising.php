<p align="left">"The elevated arms of what I like to call the 'Bearer' are for me raised both in victory and in offering. The figure for me is a classical choice for the depiction of both humanistic ideals, health, vitality and for the individual as the subject of the award. It is my hope that the combination of the raised, straitened and supportive arms in concert with the bowed head help to reinforce notions of accomplishment, prosperity and service.  For me, it is not an individual's bowl the 'Bearer' presents; it is to and for the community.  I chose the bowl to illustrate symbols of: the circle, community, inclusion, container and plenty."</p>

