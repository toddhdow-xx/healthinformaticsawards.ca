    <td colspan="2" class="b">
	<p>Criteria for consideration for the Award outlined that the innovative approach be for a program or project that has been successfully implemented, that it may not necessarily be national in scope, but can be seen as having a significant impact in a region, province or territory in Canada and the it has the ability to be replicated in other parts of the health care system. </p>
	<p>In addition, the program or project should demonstrate leading edge thinking and implementation in the subject area of the initiative, and have a positive impact on health care delivery or outcomes.  </p>
	<p>The winner of the "Innovation in the Adoption of Health Informatics" Award is North Network, a renowned Canadian and international leader in the adoption of telemedicine into the mainstream of health care services.  This non-profit, member-based organization facilitates the integration of telemedicine into everyday practice by making the technology easy to use, providing telemedicine technology expertise for all of its partners, interfacing with government and regulatory bodies and increasing awareness among health providers and the public of the benefits of telemedicine.</p>
	<p>Their ability to implement their program is based on a change management approach that is clearly "leading edge".  They understand that it is the health care providers, not the equipment, that are the backbone of the telemedicine system.  Key features of this strategy include local leadership at every site, enhanced regional communication, adaptation to First Nations communities, maintenance of existing referral patterns, comprehensive training and turnkey technology support.</p>
	<p>Their operational record is astounding - facilitating over 1200 clinical consultations monthly, over a network of 190 sites and linking over 2000 physicians - unmatched anywhere in Canada - and receiving an astonishing 95% approval rate among patients, referring physicians and specialists!  Their impact on health care delivery is such that without it, more than 18,000 patients this year would have to travel to receive care.  Putting aside the obvious positive impact on patient quality of life, consider that since 2001, the provincial government has avoided over $7M in travel subsidies. </p>
	<p>In addition, this organization supports over 250 educational sessions a month through video conferencing and webcasting, attended by upwards of 3000 participants from a diverse professional group including family physicians, specialists, nurses, rehab therapists, and other allied health professionals.  In all their endeavours, North Network is strongly committed to distance-based technologies that provide access and opportunity to education not otherwise possible.</p>
	<p>The program is easily transferable to other parts of the health care system because it is built on partnerships - not just on financial commitments, but on shared vision, common goals and active participation, all crucial to sustainable health care delivery in Canada.  Their success is in having established a process to bring everyone together.  This approach is now being used as a model by health care providers across the country. </p>

