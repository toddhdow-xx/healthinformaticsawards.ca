    <td colspan="2" class="b">
	<p>Our winner of the 2005 Leadership in the Field of Health Informatics Award, Dr. Lynn Nagle. is no stranger to our community and has been a member of COACH since 1992.  A passionate and energetic advocate who is committed to the advancement of health informatics, Dr. Nagle is viewed by colleagues and peers as a mentor who embodies vision, hard-work and perseverance.  </p>
	<p>Her main areas of research and scholarly interest are of the human experience of technology in health care, understanding the outcomes of information technology in health care delivery for consumers and health care providers, integrating informatics in the undergraduate and graduate curricula of healthcare professionals, and understanding and facilitating the uptake and adoption of technology in the health care setting.</p>
	<p>She has made significant contributions to move forward Canadian Health Informatics in general, and specifically for the nursing profession.  Dr. Nagle has a profound capacity to build both interest and enthusiasm in professional group activities and was instrumental in the successful launch of the Canadian Nursing Informatics Association (formerly COACH Nursing NISIG) to facilitate a national voice and a forum where nurses could engage in health and nursing informatics initiatives.  One such initiative has resulted in CNIA being recognized for its national study "Educating Tomorrow's Nurses: Where's Nursing Informatics?" This Study examined nursing education, nursing resources, nursing preparedness and the available information and communication technology infrastructures to facilitate informatics education planning requirements to allow nurses to successfully engage in the evolving technological health system.</p>
	<p>In addition to her demonstrated leadership in the field, she has contributed to major publications in nursing informatics and has spoken at major national and international conferences and events.  She was instrumental in launching the first Nursing Informatics course at the graduate level at the University of Toronto and continues to support and mentor students across Canada.  </p>
	<p>For the last decade, her work in informatics has focused on the identification and evaluation of appropriate information management solutions as a Senior Vice President for Technology and Knowledge Management at Mount Sinai Hospital in Toronto.  In this role, she had corporate responsibility for the organization's information and communication technology and electronic patient record strategies.  She has recently established her own consulting practice and is currently providing consulting on issues and initiatives related to nursing and health informatics.  Most recently, she was appointed as Senior Nursing Advisor to Canada Health Infoway to support the End User Adoption Strategy development and deployment.  In September, she was recognized for her leadership in informatics receiving the "Most Distinguished Alumni" award from the University of Rochester.</p>

