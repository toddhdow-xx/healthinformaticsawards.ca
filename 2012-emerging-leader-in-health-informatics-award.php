Nominations must specifically address all the following criteria. Nominations must be complete; incomplete nominations will not be considered. <BR>
<ul>
<li>Supervisors and peers have recognized the nominee for demonstrating leadership and/or innovation in his/her work. Please provide examples of the type of leadership (e.g., team leadership, thought leadership, etc.) that the nominee has demonstrated and how his/her leadership or innovation has helped improve health outcomes and/or achieve health system efficiencies. 
