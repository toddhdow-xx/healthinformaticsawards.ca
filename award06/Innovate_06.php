    <td colspan="2" class="b">
	<p>The tele-homecare approach included a formal process for technology selection and development/adoption of patient educational materials.  A store and forward, Plain Old Telephone Service (POTS) home-based monitoring system was selected.  It collects a patient's weight, temperature, blood pressure, pulse, oxygen levels, and is equipped with customizable, pre-recorded messages that ask the patient a series of subjective questions about their condition.  Vital signs are received and stored for clinical review, on a central base station computer located at the EMP office.  The software color codes and triages all data collected so that professionals can identify whether patients are doing fine or if they require a phone follow-up or immediate home visit.</p>
	<p>This system of monitoring on a daily basis helped health professionals identify warning signs or changes in a patient's condition.  This increased opportunities for early intervention and ultimately prevented complications.  After receiving this tele-homecare service, patients became more knowledgeable about their disease and learned how to better manage their condition on a daily basis.</p>
	<p>Given the growing interest in tele-homecare and in new approaches to the care of patients with a chronic disease nation wide, the work successfully completed by the implementation team from 2004 -2006 is nationally significant in its contribution to health informatics.</p>
	<p>The Project has effectively demonstrated the important role that health informatics plays within the health care system by providing evidence that daily monitoring enabled by tele-homecare, combined with a consistent education program aimed at self-management can effectively reduce hospital admissions and ER visits and improve the client's ability to self-manage his/her chronic illness.</p>
	<p>The project team has provided leadership and commitment to the advancement of health care informatics in Canada by developing a comprehensive tool kit, and making the entire product, a culmination of two years work, available on their web site.  This will allow this important work to be shared and replicated in other jurisdictions.</p>
	
