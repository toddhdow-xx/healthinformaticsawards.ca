<tr>
    <td colspan="2" class="b">
	<p>The project team developed a plan that maximized use of existing avenues of communication and informal sessions.  They approached various inherent challenges by creating separate support teams for each implementation.  Diagnostic imaging staff were recruited from various sites from around the province and temporarily re-assigned.  This ensured that the needs of small, medium and large sites were supported and fostered by a truly provincial team approach. </p>
	<p>In addition to the complexities of a multi-organizational environment, various discipline areas needed to work effectively together to support a successful transition.  District implementation teams were formed comprised of staff representing diagnostic imaging, communications/public relations, IT and facilities management.  Weekly meetings with status reports and assigned priorities provided the basis for tracking and resolution of issues with a lessons learned approach.  As a result, each implementation built on the experience of the previous and knowledge was shared across the province.</p>
	<p>Key to the success of the Project was the extensive and rigorous project planning and estimating process.  A rolling wave site plan template was created in the planning phase and was continuously updated and improved after each phase of the implementation.  The implementation phase involved regular status monitoring and detailed financial tracking throughout.</p>
	<p>The PACS Project was completed on time and within budget.  Radiologists and referring physicians reported improved turnaround time, improved ability to make decisions regarding patient care and improved efficiency.</p>

