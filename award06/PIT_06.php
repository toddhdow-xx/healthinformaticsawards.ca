    <td class="b" align="justify"><p>Picture Archiving and Communications System technology - or PACS - has replaced nearly all film-based medical imaging with faster, safer and more integrated processes.  Following an 18 month implementation, 41 health care sites in Nova Scotia are now active with PACS - making this the largest provincial jurisdiction with a fully digital imaging environment. </p>
	
