    <td colspan="2" class="b">
	<p>Xwave's depth of skill is evident in its service provision to the Atlantic Health Sciences Corporation. The firm has many years of experience in healthcare and operates a dedicated Healthcare Practice comprising 200 consultants. It is also a longtime provider of outsourced services to a number of large organizations. This particular contract is managed through a designated Core Team backed up by a considerably larger group of seasoned healthcare and outsourcing specialists. This combined experience in healthcare, outsourcing and system integration, has enabled xwave to understand AHSC's needs and respond to them in a targeted, practical manner.</p>
	<p>Drawing from the above-mentioned resources, they have brought to this engagement superior technical and business expertise, demonstrated through the clarity of service level agreements, the quality of governance, the consistency of management and performance-measurement, and the capabilities of the team.</p>
	<p>Their Clinical Management System (CMS) in particular has propelled considerable growth within the firm - beginning in 2005 with approximately 10 people, now at 50 and exhibiting steady growth as the team expands.  The CMS is now launched in Ontario, and is the only solution of its kind-offered through an application service provider (ASP) model-to be approved and funding-eligible under Ontario's Physician IT Program.</p>
	<p>And finally, to complement these efforts around health information, they are participating in related initiatives such as one involving Hamilton's McMaster University and a partnership with Intel to establish a Chair at McMaster: The Academy of Health Informatics. The Chair will ultimately see the creation of a health-informatics program at the University and, as part of that program, electronic health record (EHR) courses that will be available to students in all four of the University's schools: Business, Computer Science, Medicine, and Nursing. Through this broadened educational scope, it's hoped that health informatics will benefit from knowledge of not only medicine, but also business, information management, financial planning, and all the other factors that play an increasingly important role in the health-informatics sector.</p>
