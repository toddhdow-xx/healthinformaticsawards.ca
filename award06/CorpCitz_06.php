	<p>CHITTA has established the <strong>Corporate Citizenship Award</strong> to recognize leading for-profit healthcare ICT companies that are committed to, and passionate about, health informatics - and who have demonstrated a significant investment in contributing to the industry in Canada. To qualify for consideration of this prestigious award, the private sector company must actively participate in promoting and developing Canada's Healthcare ICT industry, and be recognized by clients and peers for having a long-term commitment to the industry.   The company supports and presents at industry conferences and events, supports educational initiatives and academic organizations, participates in standards and other infrastructure activities and maintains an involvement with industry associations, volunteer boards, and publications.</p>
