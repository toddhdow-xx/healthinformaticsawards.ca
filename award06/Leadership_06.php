	<p>She was also instrumental in securing a $3 million investment to accelerate the implementation of the system across the province.  This initiative has achieved unprecedented success in clinician adoption and is expected to significantly improve patient safety, efficiency in the delivery of systemic therapy and quality of care across the province.</p>
	<p>Sarah's success with the CPOE initiative has been paralleled with her concurrent leadership in Ontario's Wait Time Strategy.  Her primary directive was to create and implement a provincial wait list system, the Wait Time Information System (WTIS), to track, measure and report on wait times for patients between the time of decision to treat and actual treatment.  This system was also designed to be used as a tool for hospitals to identify and reduce wait times.  Through her capacity as Lead for Ontario's Wait Time Information Strategy and Client Registry Initiative, she developed an approach to evaluate existing systems, introduced a prospective data collection system, and continues to lead the implementation of the WTIS in hospitals across Ontario.</p>
	<p>Despite carrying the dual roles, Sarah has been careful to contribute back to the health informatics community by sharing her experiences and lessons learned with regular contributions to the e-Health Conference and Waterloo's Institute for Health Informatics Research.</p>
	<p>In short, her dedication and leadership has been vital to moving forward the heath informatics field and achieving the technical and clinical goals at provincial and national levels.</p>
