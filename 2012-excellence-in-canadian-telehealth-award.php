<B>Criteria</B><BR>
<BR>
Nominations are submitted by Telehealth members and the recipient is selected by the Adjudication Committee based upon specific criteria. The program, project or service provider:<BR>
<ul>
<li>Serves as a forum for the collection, exchange and dissemination of information related to telehealth;  
<li>Interfaces with other organizations, institutions, governments, governmental organization, individuals, societies and corporations involved or concerned with the development and implementation of telehealth activities, healthcare and healthcare services and encourages cooperation;  
<li>Assists in the education of individuals, organizations, corporations and societies with respect to telehealth;  
<li>Promotes and encourages the use of telecommunications and related technologies in the delivery of primary care and continuing education;  
<li>Promotes the use of telehealth in order to improve the healthcare delivery system, to assist in the improvement of access to healthcare delivery and to benefit the participants in healthcare systems;  
<li>Encourages telehealth research;  
<li>Actively promotes the advancement of telehealth, including but not limited to, telehealth technologies and their application for the benefit of their clients;  
<li>Is a model of the integration of telehealth into the organization's delivery of services or encourages and demonstrates an ability to integrate telehealth services into the Electronic Health Record. 
</ul>

<B>Eligibility</B><BR>
<ul>
<li>Recipients of these awards are restricted to telehealth programs, projects or service providers operating in Canada who have developed or who lever telehealth technology to provide clinical, educational or administrative support to the health care sector;
<li>No individuals or for-profit corporations quality for these awards.
</ul>
