	   <p align="left">The 6<sup>th</sup> annual CANADIAN HEALTH INFORMATICS AWARDS (CHIA) GALA, proudly co-hosted by <a href="http://www.itac.ca/health" target="_blank">ITAC Health</a>, the Information Technology Association of Canada, and <a href="http://www.coachorg.com" target="_blank">COACH</a>: Canada's Health Informatics Association, will be staged this year at the Four Seasons Hotel in Toronto on Thursday, November 18, 2010.</p>

<p align="left">Celebrating the outstanding achievements and excellence in the Canadian Health Informatics industry, the CHIA Gala recognizes and honours the contributions of individuals and companies in the efficient use of information technology to improve the health of all Canadians.</p> 

<p align="left">This elegant evening will feature a cocktail reception followed by a sumptuous four course dinner with wine and the presentation of awards. The CHIA Gala rolls out the red carpet and puts the celebrated nominees, winners and other industry achievers <b><em>"In The Spotlight"</em></b>.</p>

