<p align="left">"I look forward to welcoming colleagues to an inspiring evening as we honour our health care IT sector's achievements," commented Michael Green, Chair, ITAC Health Board. "It will be my pleasure as Chair of ITAC Health to join this celebration of our partnership with COACH and the innovations our acclaimed Canadian IT community develops to advance the quality of health care."</p>

