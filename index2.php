<p align="left">"These awards are an excellent opportunity to recognize outstanding achievements taking place in Canadian health care," said Lydia Lee, President and Board Chair, COACH. "The further addition of the telehealth awards to the program is an important reflection of the vital contribution these professionals make to the health care system. COACH is also delighted to start offering both a graduate and an undergraduate Steven Huesing Scholarship to support the health informatics professionals of the future."</p>

<p align="left">ITAC Health presents awards to: Company of the Year, Corporate Citizenship: Multi-National Company, Major Canadian Company and Small to Medium Enterprise, Healthcare Transformation and Project Implementation Team of the Year.</p>

