<p align="left">The CHIA Gala, one of Canada's leading eHealth industry events, honours leaders and innovators with the following COACH awards:</p>

<p align="left">Best Innovation in Technology & Learning, Community-Based Physician Leader/Innovator, Emerging Leader in Health Informatics, Excellence in Canadian Telehealth, Innovation in the Adoption of Health Informatics, Innovative Use of Store & Forward Technology, Leadership in the Field of Health Informatics, Telehealth Technology Innovation and Steven Huesing Scholarship.</p>

