<p align="left">The 7<sup>th</sup> annual CANADIAN HEALTH INFORMATICS AWARDS (CHIA) GALA, proudly co-hosted by <a href="http://www.itac.ca/health" target="_blank">ITAC Health</a>, the Information Technology Association of Canada, and <a href="http://www.coachorg.com" target="_blank">COACH</a>: Canada's Health Informatics Association, will be hosted this year on Wednesday, November 9, 2011 at the Royal York Hotel, 100 Front Street West, Toronto</p>

<p align="left">Cocktails at 6 pm, followed by Dinner & Awards Presentation</p>

<p align="left"><B><U>COACH & ITAC Health Awards</u></B></p>
<p align="left"><a href="http://www.healthinformaticsawards.ca/awards_nominations.php">Call for nominations</a></p>
<p align="left">Watch for Call for Nominations this Spring!</p>

<p align="left">Celebrating the outstanding achievements and excellence in the Canadian Health Informatics industry, the CHIA Gala recognizes and honours the contributions of individuals and companies in the efficient use of information technology to improve the health of all Canadians.</p>

<p align="left">The CHIA Gala, one of Canada's leading eHealth industry events, honours leaders and innovators with the following COACH awards:</p>
<p align="left"><i>"This is a wonderful opportunity to roll out the red carpet to those who are making amazing advances in making IT work in hhealthcare for the betterment of all Canadians."</i></p>
<p align="left">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- Lydia Lee, President & Board Chair, COACH</p>

<p align="left"><i>"I look forward to welcoming colleagues to an inspiring evening as we honour our health care IT sector's achievements It will be my pleasure as Chair of ITAC Health to join this celebration of our partnership with COACH and the innovations our acclaimed Canadian IT community develops to advance the quality of health care."</i></p>
<p align="left">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- Michael Green, Chair, ITAC Health Board.</p>

<p align="left">The 2011 CHIA Gala promises to be a sold out affair that offers a selection of tiered sponsorship opportunities which maximize exposure to the eHealth industry for sponsors of this prestigious event.  This gala will be widely promoted across Canada and is a significant opportunity to gain valuable exposure with our high-profile attendees and the eHealth industry.</p>

