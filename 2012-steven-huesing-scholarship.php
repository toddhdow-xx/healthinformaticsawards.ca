The Steven Huesing Scholarship was established in 1999 in recognition of the contribution that the late Steven Huesing, COACH Founding President, made to the association. The scholarship was developed to reflect the spirit, dedication and innovation that the Founding President brought to the field of health informatics (HI).<BR>
<BR>
The scholarship consists of:<BR>
<ul>
<li>A cash honorarium of $1,000; 
<li>A one-year COACH Student Membership; 
<li>A Student Registration for the next <a href="http://www.e-healthconference.com/">e-Health conference</a>. 
</ul>
The scholarship is available for both an undergraduate student and a graduate student.<BR>
Undergraduate: <a href="http://coach.survey.esolutionsgroup.ca/TakeSurvey.aspx?SurveyID=llKL664">SUBMIT NOW</a><BR>
Graduate: <a href="http://coach.survey.esolutionsgroup.ca/TakeSurvey.aspx?SurveyID=llKL764">SUBMIT NOW</a><BR>
<BR>
<B>Criteria</B><BR>
Applications must specifically address all the following criteria and incomplete applications will not be considered. The applicant should:<BR>
<ul>
<li>Be of sound academic standing (See Application Requirements below); 
<li>Demonstrate active involvement and achievement in HI;  
<li>Show how his/her educational work has made a recognized contribution to advancing the thinking in HI. 
</ul>
<B>Eligibility</B><BR>
Scholarship applications are accepted from any student currently enrolled in a health informatics (HI) or related program at an accredited post-secondary institution.<BR>
Application Requirements<BR>
The following must be submitted with the application:<BR>
<ul>
<li>A transcript of marks;  
<li>An assessment by an academic advisor; 
<li>Proof of enrollment in and current attendance at a recognized Canadian post-secondary institution in a HI or related program; 
<li>Any other information to support the application.
</ul>
<B>Recognition</B><BR>
The scholarship will be presented at the <a href="http://www.healthinformaticsawards.ca/">Canadian Health Informatics Awards Gala</a> in Toronto Nov. 7, 2012.<BR>
<BR>
<B>Deadline: 5 pm EDT Sept. 17</B><BR>
